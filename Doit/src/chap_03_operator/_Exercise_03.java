package chap_03_operator;

public class _Exercise_03 {
    public static void main(String[] args) {
        int a = 5;
        int b = a++;
        System.out.println(a);
        System.out.println(b);
        System.out.println(++a);
        System.out.println(++b);
        System.out.println(a++ + --b);
        System.out.println(a + b);


    }
}
